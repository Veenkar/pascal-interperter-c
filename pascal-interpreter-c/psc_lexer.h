/*******************************************************************************
 * Type Declarations
 ******************************************************************************/
typedef struct Psc_Lexer_Tag
{
    char * text;
    size_t pos;
    bool   eof;

} Psc_Lexer_T;

/*******************************************************************************
 * Object Declarations
 ******************************************************************************/

/*******************************************************************************
 * Functions Declarations
 ******************************************************************************/
Psc_Lexer_T Psc_Lexer(char *text)
{
    Psc_Lexer_T obj = {0};
    obj.text        = text;
    obj.pos         = 0;
    obj.eof         = (NULL == text) ? true : false;
    return obj;
}

